#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>

using namespace std;

const int N = 110000;
int arr[N];

int main()
{
	int T, n, m;
	scanf("%d", &T);
	while(T--){
		scanf("%d", &n);
		for(int i = 0; i < n; i++)
		  scanf("%d", &arr[i]);
		scanf("%d", &m);
		int l, r;
		while(m--){
			scanf("%d%d", &l, &r);
		}
	}

	return 0;
}
